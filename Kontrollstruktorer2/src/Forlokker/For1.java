package Forlokker;

public class For1 {
    public static void main(String[] args){

        int i = 0;
        while (i<10){
            System.out.print(i + " ");
            i++;
        }
        System.out.println();
        for (int j = 0;j<10; j++){
            System.out.print(j + " ");

        }

    }
}
